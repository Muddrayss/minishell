#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*first;
	t_list	*new;
	void	*res;

	if (!f || !del)
		return (NULL);
	first = NULL;
	while (lst)
	{
		res = f(lst->content);
		new = ft_lstnew(res);
		if (!new)
		{
			del(res);
			ft_lstclear(&first, del);
			return (NULL);
		}
		ft_lstadd_back(&first, new);
		lst = lst->next;
	}
	return (first);
}
