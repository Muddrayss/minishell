#include "../../../headers/minishell.h"

int16_t	ft_strcmp(t_cc *s1, t_cc *s2)
{
	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
