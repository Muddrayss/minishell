#include "../../../headers/minishell.h"

char	*ft_strcat(char *const dest, t_cc *const src)
{
	uint16_t	i;
	uint16_t	j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
